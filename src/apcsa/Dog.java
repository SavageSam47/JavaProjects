package apcsa;

public class Dog {

	private String name;
	private int age;
	private double weight;
	
	public String getName() {
		return name;
	}
	
	public int getAge() {
		return age;
	}
	
	public double getWeight() {
		return weight;
	}
	
	public void setName(String nn) {
		name = nn;
	}
	
	public void setAge(int nn) {
		age = nn;
	}
	
	public void setWeight(double nn) {
		weight = nn;
	}
	
}
